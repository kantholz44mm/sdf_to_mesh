//
// Lookup Tables for Marching Cubes
//
// These tables differ from the original paper (Marching Cubes: A High Resolution 3D Surface Construction Algorithm)
//
// The co-ordinate system has the more convenient properties:
//
//    i = cube index [0, 7]
//    x = (i & 1) >> 0
//    y = (i & 2) >> 1
//    z = (i & 4) >> 2
//
// Axes are:
//
//      y
//      |     z
//      |   /
//      | /
//      +----- x
//
// Vertex and edge layout:
//
//            6             7
//            +-------------+               +-----6-------+   
//          / |           / |             / |            /|   
//        /   |         /   |          11   7         10   5
//    2 +-----+-------+  3  |         +-----+2------+     |   
//      |   4 +-------+-----+ 5       |     +-----4-+-----+   
//      |   /         |   /           3   8         1   9
//      | /           | /             | /           | /       
//    0 +-------------+ 1             +------0------+         
//
// Triangulation cases are generated prioritising rotations over inversions, which can introduce non-manifold geometry.

fn marching_cubes(sdf: &SDFNode, center: Vec3, size: Vec3, resolution: f32, stlfile: &str) {
    
    let numsteps = (size / resolution).ceil().as_ivec3();

    let neighbors = [
        IVec3::new(0, 0, 0),
        IVec3::new(1, 0, 0),
        IVec3::new(0, 1, 0),
        IVec3::new(1, 1, 0),
        IVec3::new(0, 0, 1),
        IVec3::new(1, 0, 1),
        IVec3::new(0, 1, 1),
        IVec3::new(1, 1, 1),
    ];

    let mut vertices: Vec<Vec3> = Vec::new();

    let start = center - (size - resolution) / 2.0;
    let mut samples: Vec<f32> = Vec::new();
    samples.resize(((numsteps.x + 1) * (numsteps.y + 1) * (numsteps.z + 1)) as usize, 0.0);

    for x in 0..=numsteps.x {
        for y in 0..=numsteps.y {
            for z in 0..=numsteps.z {
                let position = start + Vec3::new(x as f32, y as f32, z as f32) * resolution;
                samples[(x + y * (numsteps.x + 1) + z * (numsteps.x + 1) * (numsteps.y + 1)) as usize] = sdf.evaluate_sdf(position);
            }
        }
    }

    for x in 0..numsteps.x {
        for y in 0..numsteps.y {
            for z in 0..numsteps.z {
                let position = start + resolution / 2.0 + Vec3::new(x as f32, y as f32, z as f32) * resolution;
                let mut neighbor_samples: [f32; 8] = [0.0; 8];
                let mut accumulator: usize = 0;
                for (index, neighbor) in neighbors.iter().enumerate() {
                    let gindex = (x + neighbor.x + (y + neighbor.y) * (numsteps.x + 1) + (z + neighbor.z) * (numsteps.x + 1) * (numsteps.y + 1)) as usize;
                    neighbor_samples[index] = samples[gindex];
                    if neighbor_samples[index] > 0.0 {
                        accumulator += 1 << index;
                    }
                }
                
                let edges = MC_TABLE[accumulator];
                // emit geometry
                for triangle in edges.chunks(3) {
                    if triangle[0] == -1 {
                        break;
                    }
                    let triedges = [MC_EDGES[triangle[0] as usize], MC_EDGES[triangle[1] as usize], MC_EDGES[triangle[2] as usize]];
                    for edge in triedges.iter() {
                        let t = neighbor_samples[edge[0] as usize] / (neighbor_samples[edge[0] as usize] - neighbor_samples[edge[1] as usize]);
                        let trivert = (neighbors[edge[0] as usize].as_vec3() + t * (neighbors[edge[1] as usize].as_vec3() - neighbors[edge[0] as usize].as_vec3())) * resolution;
                        vertices.push(position + trivert);
                    }
                }
            }
        }
        println!("about {}% done.", 100.0 * (x as f32 / numsteps.x as f32));
    }

    println!("done. writing mesh file...");
    write_stl(stlfile, &vertices);
    println!("done.")
}

fn convert_vertlist_to_mesh(vertices: &Vec<Vec3>) -> Vec<Triangle> {
    
    let mut triangles: Vec<Triangle> = Vec::new();

    for tri in vertices.chunks(3) {
        let a = tri[0] - tri[1];
        let b = tri[0] - tri[2];
        let normal = a.cross(b);

        triangles.push(Triangle {
            normal: Vector::new(normal.to_array()),
            vertices: [
                Vector::new(tri[0].to_array()),
                Vector::new(tri[1].to_array()),
                Vector::new(tri[2].to_array())
            ]
        });
    }
    
    triangles
}

fn write_stl(filename: &str, vertices: &Vec<Vec3>) {
    let mut mesh = convert_vertlist_to_mesh(&vertices);
    let mut file = OpenOptions::new().write(true).truncate(true).create(true).open("mesh.stl").expect("could not open file for writing");
    //stl_io::write_stl(&mut file, [mesh].iter());
    stl_io::write_stl(&mut file, mesh.iter());
}

fn write_obj(filename: &str, vertices: &Vec<Vec3>, indices: &Vec<u32>) {
    if let Ok(mut file) = File::create(filename) {
        let start = Instant::now();
        let mut bytes: Vec<u8> = Vec::new();
        for vertex in vertices.iter() {
            bytes.extend(format!("v {} {} {}\n", vertex.x, vertex.y, vertex.z).as_bytes());
        }
        file.write_all(bytes.as_slice()).expect("failed writing to file");
        let elapsed = start.elapsed();
        println!("elapsed during writing: {:?}", elapsed);

        for face in indices.chunks_exact(3) {
            file.write_all(format!("f {} {} {}\n", face[0] + 1, face[1] + 1, face[2] + 1).as_bytes()).expect("failed writing to file");
        }
    }
}
