# ─── Primitives ──────────────────────────────────────────────────────────────

def sphere(radius: float = 1.0, center=(0.0, 0.0, 0.0)) -> SDF:
    c = np.asarray(center, dtype=float)
    def _sphere(p):
        p = np.atleast_2d(p)
        return (np.linalg.norm(p - c, axis=-1) - radius).squeeze()
    return _sphere

def box(size) -> SDF:
    s = np.asarray(size, dtype=float)
    def _box(p):
        p = np.atleast_2d(p)
        q = np.abs(p) - s
        return (np.linalg.norm(np.maximum(q, 0.0), axis=-1) + np.minimum(0.0, q.max(axis=-1))).squeeze()
    return _box

def cylinder(radius: float, height: float) -> SDF:
    h = height / 2.0
    def _cyl(p):
        p = np.atleast_2d(p)
        d = np.abs(np.stack([np.linalg.norm(p[:, [0, 2]], axis=-1), p[:, 1]], axis=-1)) - np.array([radius, h])
        return (np.linalg.norm(np.maximum(d, 0.0), axis=-1) + np.minimum(0.0, d.max(axis=-1))).squeeze()
    return _cyl

def capsule(radius: float, height: float) -> SDF:
    h = height / 2.0
    def _cap(p):
        p = np.atleast_2d(p).copy()
        p[:, 1] -= np.clip(p[:, 1], -h, h)
        return (np.linalg.norm(p, axis=-1) - radius).squeeze()
    return _cap

def torus(major_radius: float, minor_radius: float) -> SDF:
    def _torus(p):
        p = np.atleast_2d(p)
        q = np.stack([np.linalg.norm(p[:, [0, 2]], axis=-1) - major_radius, p[:, 1]], axis=-1)
        return (np.linalg.norm(q, axis=-1) - minor_radius).squeeze()
    return _torus

def plane(normal=(0.0, 1.0, 0.0), offset: float = 0.0) -> SDF:
    n = np.asarray(normal, dtype=float)
    n = n / np.linalg.norm(n)
    def _plane(p):
        p = np.atleast_2d(p)
        return (p @ n + offset).squeeze()
    return _plane

# ─── Transforms ──────────────────────────────────────────────────────────────

def translate(sdf: SDF, offset) -> SDF:
    o = np.asarray(offset, dtype=float)
    return lambda p: sdf(np.atleast_2d(p) - o)

def rotate(sdf: SDF, axis, angle: float) -> SDF:
    ax = np.asarray(axis, dtype=float)
    ax = ax / np.linalg.norm(ax)
    c, s = np.cos(angle), np.sin(angle)
    x, y, z = ax
    # Rodrigues rotation matrix; applying R to domain points rotates the object by +angle
    R = np.array([
        [c + x*x*(1-c),     x*y*(1-c) - z*s,  x*z*(1-c) + y*s],
        [y*x*(1-c) + z*s,   c + y*y*(1-c),    y*z*(1-c) - x*s],
        [z*x*(1-c) - y*s,   z*y*(1-c) + x*s,  c + z*z*(1-c)  ],
    ])
    return lambda p: sdf(np.atleast_2d(p) @ R)

def scale(sdf: SDF, factor: Union[float, tuple, list, np.ndarray]) -> SDF:
    f = np.asarray(factor, dtype=float)
    if f.ndim == 0:
        return lambda p: sdf(np.atleast_2d(p) / f) * float(f)
    # Non-uniform: divide each axis; multiply by min factor as SDF approximation
    min_f = float(f.min())
    return lambda p: sdf(np.atleast_2d(p) / f) * min_f

def mirror(sdf: SDF, axis: Union[str, int] = 'x') -> SDF:
    ax = {'x': 0, 'y': 1, 'z': 2}[axis] if isinstance(axis, str) else int(axis)
    def _mirror(p):
        p = np.atleast_2d(p).copy()
        p[:, ax] = np.abs(p[:, ax])
        return sdf(p)
    return _mirror

def elongate(sdf: SDF, size) -> SDF:
    h = np.asarray(size, dtype=float)
    def _elongate(p):
        p = np.atleast_2d(p)
        q = p - np.clip(p, -h, h)
        return sdf(q)
    return _elongate

def twist(sdf: SDF, k: float) -> SDF:
    def _twist(p):
        p = np.atleast_2d(p)
        c = np.cos(k * p[:, 1])
        s = np.sin(k * p[:, 1])
        qx = c * p[:, 0] - s * p[:, 2]
        qz = s * p[:, 0] + c * p[:, 2]
        return sdf(np.stack([qx, p[:, 1], qz], axis=-1))
    return _twist

# ─── Booleans ────────────────────────────────────────────────────────────────

def union(a: SDF, b: SDF) -> SDF:
    return lambda p: np.minimum(a(p), b(p))

def intersection(a: SDF, b: SDF) -> SDF:
    return lambda p: np.maximum(a(p), b(p))

def difference(a: SDF, b: SDF) -> SDF:
    return lambda p: np.maximum(a(p), -b(p))

# ─── Smooth operations ───────────────────────────────────────────────────────

def smooth_union(a: SDF, b: SDF, k: float) -> SDF:
    def _su(p):
        d1, d2 = a(p), b(p)
        h = np.clip(0.5 + 0.5 * (d2 - d1) / k, 0.0, 1.0)
        return d2 + h * (d1 - d2) - k * h * (1.0 - h)
    return _su

def smooth_intersection(a: SDF, b: SDF, k: float) -> SDF:
    def _si(p):
        d1, d2 = a(p), b(p)
        h = np.clip(0.5 - 0.5 * (d2 - d1) / k, 0.0, 1.0)
        return d2 + h * (d1 - d2) + k * h * (1.0 - h)
    return _si

def smooth_difference(a: SDF, b: SDF, k: float) -> SDF:
    def _sd(p):
        d1, d2 = a(p), b(p)
        h = np.clip(0.5 - 0.5 * (d2 + d1) / k, 0.0, 1.0)
        return d2 + h * (-d1 - d2) + k * h * (1.0 - h)
    return _sd

# ─── Domain operations ───────────────────────────────────────────────────────

def repeat(sdf: SDF, period, count) -> SDF:
    """Finite repetition. period and count are per-axis (vec3)."""
    c = np.asarray(period, dtype=float)
    n = np.asarray(count, dtype=float)
    def _rep(p):
        p = np.atleast_2d(p)
        q = p - c * np.clip(np.round(p / c), -n, n)
        return sdf(q)
    return _rep

def onion(sdf: SDF, thickness: float) -> SDF:
    return lambda p: np.abs(sdf(p)) - thickness

def offset(sdf: SDF, amount: float) -> SDF:
    return lambda p: sdf(p) - amount

# ─── Marching cubes ──────────────────────────────────────────────────────────

def marching_cubes(
    sdf: SDF,
    center=(0.0, 0.0, 0.0),
    size=(1.0, 1.0, 1.0),
    resolution: float = 0.05,
    filename: str = "mesh.stl",
):
    center = np.asarray(center, dtype=float)
    size = np.asarray(size, dtype=float)
    numsteps = np.ceil(size / resolution).astype(int)
    nx, ny, nz = numsteps
    start = center - (size - resolution) / 2.0

    xi = np.arange(nx + 1, dtype=float)
    yi = np.arange(ny + 1, dtype=float)
    zi = np.arange(nz + 1, dtype=float)
    gx, gy, gz = np.meshgrid(xi, yi, zi, indexing='ij')
    pts = start + np.stack([gx, gy, gz], axis=-1) * resolution
    print("Sampling SDF...")
    samples = sdf(pts.reshape(-1, 3)).reshape(nx + 1, ny + 1, nz + 1)

    vertices = []

    print("Running marching cubes...")
    for x in range(nx):
        for y in range(ny):
            for z in range(nz):
                position = start + resolution / 2.0 + np.array([x, y, z], dtype=float) * resolution

                ns = np.array([
                    samples[x + o[0], y + o[1], z + o[2]]
                    for o in _NEIGHBOR_OFFSETS_INT
                ])

                acc = int(np.sum(_CORNER_BITS[ns > 0]))
                edges = _MC_TABLE[acc]

                i = 0
                while i < 12 and edges[i] != -1:
                    for k in range(3):
                        e = _MC_EDGES[edges[i + k]]
                        s0, s1 = ns[e[0]], ns[e[1]]
                        t = s0 / (s0 - s1)
                        v = (_NEIGHBOR_OFFSETS_F[e[0]] + t * (_NEIGHBOR_OFFSETS_F[e[1]] - _NEIGHBOR_OFFSETS_F[e[0]])) * resolution
                        vertices.append(position + v)
                    i += 3

        print(f"  {100 * (x + 1) / nx:.0f}%", end='\r')

    print(f"\nWriting {len(vertices) // 3} triangles to {filename}...")
    _write_stl(filename, vertices)
    print("Done.")

# ─── STL output ──────────────────────────────────────────────────────────────

def _write_stl(filename: str, vertices: list):
    tris = [vertices[i:i + 3] for i in range(0, len(vertices), 3)]
    with open(filename, 'wb') as f:
        f.write(b'\0' * 80)
        f.write(struct.pack('<I', len(tris)))
        for tri in tris:
            a = np.asarray(tri[0]) - np.asarray(tri[1])
            b = np.asarray(tri[0]) - np.asarray(tri[2])
            normal = np.cross(a, b)
            length = np.linalg.norm(normal)
            if length > 0:
                normal /= length
            f.write(struct.pack('<3f', *normal.astype(np.float32)))
            for v in tri:
                f.write(struct.pack('<3f', *np.asarray(v, dtype=np.float32)))
            f.write(struct.pack('<H', 0))

